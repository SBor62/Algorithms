#       Алгоритм для проверки строки на палиндром:
# 1. Привести строку к единому регистру (например, нижнему),
# 2. Удалить из строки все пробелы и знаки препинания, если они не учитываются при проверке.
# 3. Сравнить исходную строку с её перевёрнутой версией:
#   	- Если они идентичны, строка — палиндром.
#       - Если нет, строка не является палиндромом.

import re


def is_palindrome(s: str) -> bool:
    # Удаляем все небуквенные и нечисловые символы и приводим к нижнему регистру
    cleaned_str = re.sub(r'[^a-zA-Zа-яА-Я0-9]', '', s.lower())
    # Сравниваем строку с её перевёрнутой версией
    return cleaned_str == cleaned_str[::-1]


# Запрашиваем ввод у пользователя
user_input = input("Введите слово или фразу для проверки на палиндром: ")

# Проверяем и выводим результат
if is_palindrome(user_input):
    print("✅ Это палиндром!")
else:
    print("❌ Это не палиндром.")
